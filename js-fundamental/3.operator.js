// Math
const addValue = 10 + 5;
const subValue = 10 - 5;
const mulValue = 10 * 5;
const divValue = 10 / 5;
const modValue = 10 % 5;
const powValue = 10 ** 5;

console.log(addValue);
console.log(subValue);
console.log(mulValue);
console.log(divValue);
console.log(modValue);
console.log(powValue);

// Increment and Decrement
let count = 0;
console.log(count);     // 0
console.log(++count);   // 1
console.log(count++);   // 1
console.log(count);     // 2
count--;
console.log(count);     // 1

// Relational Operator
// <, >, <=, >=, !=, ===
console.log('1' != 1);  // false
console.log('1' !== 1); // true

const exampleCondition = 20 <= 10;
console.log(`Condittion result: ${exampleCondition}`);

const firstCondition = 2000 === 2000;
console.log(`Condittion result: ${firstCondition}`);

const secondCondition = 'Hello' === 'Hi';
console.log(`Condittion result: ${secondCondition}`);